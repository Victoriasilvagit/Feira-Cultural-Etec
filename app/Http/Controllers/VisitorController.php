     */
    public function store(VisitorRequest $request)
    {
        $cad=Visitor::create([
            'name'=>$request->name,
            'email'=>$request->email,
            'cellphone'=>$request->cellphone,
            'friend_family'=>$request->friend_family,
        ]);

        if($cad)
            return redirect('admin/visitors/create')->with('message', 'CADASTRO REALIZADO COM SUCESSO!');
    }

